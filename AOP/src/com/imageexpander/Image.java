package com.imageexpander;

public class Image {
	
	private Integer imageId;
	private String description;
	private String flickrSize;
	private Boolean fromApi;
	private String gettyLocation;
	private String gettySize;
	private String mainColor;
	private String title;
	private String uri;
	
	public Integer getImageId() {
		return imageId;
	}
	public void setImageId(Integer imageId) {
		this.imageId = imageId;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getFlickrSize() {
		return flickrSize;
	}
	public void setFlickrSize(String flickrSize) {
		this.flickrSize = flickrSize;
	}
	public Boolean getFromApi() {
		return fromApi;
	}
	public void setFromApi(Boolean fromApi) {
		this.fromApi = fromApi;
	}
	public String getGettyLocation() {
		return gettyLocation;
	}
	public void setGettyLocation(String gettyLocation) {
		this.gettyLocation = gettyLocation;
	}
	public String getGettySize() {
		return gettySize;
	}
	public void setGettySize(String gettySize) {
		this.gettySize = gettySize;
	}
	public String getMainColor() {
		return mainColor;
	}
	public void setMainColor(String mainColor) {
		this.mainColor = mainColor;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getUri() {
		return uri;
	}
	public void setUri(String uri) {
		this.uri = uri;
	}
	
}
